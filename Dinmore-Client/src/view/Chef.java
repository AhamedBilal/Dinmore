    private void loadAll() {
        DefaultTableModel dtm = (DefaultTableModel) tbl.getModel();
        dtm.setRowCount(0);
        try {
            List<OrderDTO> re = OrderController.getAllOrders();
            if (re == null) {
                return;
            }

            for (OrderDTO o : re) {
                if (date.equals(o.getOrderDate()) && o.getStatus() == 1) {
                    Object[] rowData = {
                        o.getOrderID(),
                        o.getRid(),
                        o.getName(),
                        o.getTel(),
                        o.getQty()
                    };
                    dtm.addRow(rowData);
                }
            }

        } catch (Exception ex) {
            Logger.getLogger(Admin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
