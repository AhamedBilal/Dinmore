    private void btn_startActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_startActionPerformed
        try {
            OrderController.updateChef(oid, 2, txt_cid.getText());
            timestoper = true;
            tbl.setRowSelectionAllowed(false);
            btn_finish.setEnabled(true);
            btn_start.setEnabled(false);
        } catch (Exception ex) {
            Logger.getLogger(Chef.class.getName()).log(Level.SEVERE, null, ex);
        }
        new Thread() {
            public void run() {
                while (timestoper) {
                    try {
                        sleep(1000);
                        s++;
                        if (s > 60) {
                            s = 0;
                            m++;
                        }
                        if (m > 60) {
                            m = 0;
                            h++;
                        }
                        txt_timer.setText(String.format("%02d", h) + ":" + String.format("%02d", m) + ":" + String.format("%02d", s));
                    } catch (InterruptedException ex) {
                        Logger.getLogger(Chef.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }

        }.start();
    }//GEN-LAST:event_btn_startActionPerformed

    private void btn_finishActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_finishActionPerformed
        timestoper = false;
        try {
            boolean b = OrderController.finishOrder(oid, 3, txt_timer.getText());
            OrderDTO o =OrderController.searchOrder(oid);
            
            if (b && o!=null) {
                HashMap map = new HashMap();
                
                map.put("oid", o.getOrderID());
                map.put("name", o.getName());
                map.put("tel", o.getTel());
                map.put("time", o.getDuration());
                map.put("rid", o.getRid());
                map.put("cid", o.getCid());
                map.put("qty", o.getQty());
                
                
                tbl.setRowSelectionAllowed(true);
                btn_start.setEnabled(true);
                btn_finish.setEnabled(false);
                JOptionPane.showMessageDialog(this, "Complete");
                 
                
            }else{
                JOptionPane.showMessageDialog(this, "Failed");
            }
        } catch (Exception ex) {
            Logger.getLogger(Chef.class.getName()).log(Level.SEVERE, null, ex);
        }
        txt_timer.setText("00:00:00");
    }//GEN-LAST:event_btn_finishActionPerformed

